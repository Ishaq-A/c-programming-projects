#include <stdio.h>

int main() {
    int num1 = 5;
    int num2 = 6;
    float num3 = 6.5;
    double num4 = 7.01;
    char str[] = "Character String";

    printf("\nnum1 is: %d\n", num1);
    printf("num2 is: %d\n", num2);
    printf("num3 is: %f\n", num3);
    printf("num4 is: %f\n", num4);
    printf("str is: %s\n", str);

    printf("\n");
    return 0;
}

